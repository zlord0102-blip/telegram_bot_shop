  v_bonus_quantity integer := 0;
  v_required_stock integer;
  v_reservation_ids bigint[];
  v_stock_count integer;
  v_hold_until timestamptz := now() + make_interval(mins => greatest(1, coalesce(p_hold_minutes, 10)));
  v_order_id bigint;
  v_user_limit integer;
  v_user_existing_quantity integer := 0;
