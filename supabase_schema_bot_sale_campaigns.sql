      select coalesce(d.sale_paid_quantity, d.quantity, 0) as qty
      from public.direct_orders d
      where d.user_id = p_user_id
        and d.sale_item_id = p_sale_item_id
        and d.status = 'pending'
    ) used;

    if v_user_existing_quantity + v_quantity > v_user_limit then
      raise exception 'sale_user_limit_exceeded';
    end if;
  end if;

  if v_required_stock > coalesce(v_sale.stock, 0) then
    raise exception 'not_enough_stock';
  end if;

  with locked_stock as (
    select r.id as reservation_id
    from public.sale_stock_reservations r
    join public.stock s on s.id = r.stock_id
    where r.sale_item_id = p_sale_item_id
      and r.released_at is null
      and r.status in ('available', 'held')
      and (r.status = 'available' or coalesce(r.held_until, now() - interval '1 second') <= now())
      and coalesce(s.sold, false) = false
    order by r.id
    for update skip locked
    limit v_required_stock
  )
  select array_agg(ls.reservation_id order by ls.reservation_id), count(*)
  into v_reservation_ids, v_stock_count
  from locked_stock ls;

  if coalesce(v_stock_count, 0) < v_required_stock then
    raise exception 'not_enough_stock';
  end if;

  insert into public.direct_orders (
    user_id,
    product_id,
    quantity,
    bonus_quantity,
    unit_price,
    amount,
    code,
    payment_channel,
    created_at,
    sale_campaign_id,
    sale_item_id,
    sale_original_unit_price,
    sale_unit_price,
    sale_discount_amount,
    sale_paid_quantity,
    sale_bonus_quantity,
    sale_snapshot,
    sale_hold_until
  )
  values (
    p_user_id,
    v_sale.product_id,
    v_quantity,
    v_bonus_quantity,
    v_sale.price,
    v_sale.price * v_quantity,
    p_code,
    'vietqr',
    now(),
    v_sale.sale_campaign_id,
    p_sale_item_id,
    coalesce(v_sale.original_price, v_sale.price),
    v_sale.price,
    greatest(0, coalesce(v_sale.original_price, v_sale.price) - v_sale.price),
    v_quantity,
    v_bonus_quantity,
    jsonb_build_object(
      'campaign_id', v_sale.sale_campaign_id,
      'campaign_name', v_sale.campaign_name,
      'sale_item_id', p_sale_item_id,
      'product_id', v_sale.product_id,
      'product_name', v_sale.name,
      'sale_price_vnd', v_sale.price,
      'original_price_vnd', v_sale.original_price,
      'ends_at', v_sale.ends_at
    ),
    v_hold_until
  )
  returning id into v_order_id;

  update public.sale_stock_reservations
  set status = 'held',
      held_direct_order_id = v_order_id,
      held_until = v_hold_until
  where id = any(v_reservation_ids);

  return query
  select
    v_order_id,
    v_sale.product_id,
    v_sale.name,
    max(s.value) filter (where s.key = 'bank_name') as bank_name,
    max(s.value) filter (where s.key = 'account_number') as account_number,
    max(s.value) filter (where s.key = 'account_name') as account_name,
    v_quantity,
    v_bonus_quantity,
    v_sale.price,
    v_sale.price * v_quantity,
    p_code,
    v_sale.sale_campaign_id,
    p_sale_item_id,
    v_hold_until
  from public.settings s;
end;
$$;

create or replace function public.create_binance_sale_direct_order(
  p_user_id bigint,
  p_sale_item_id bigint,
  p_quantity integer,
  p_code text,
  p_payment_asset text,
  p_payment_network text,
  p_payment_amount_asset numeric,
  p_payment_rate_vnd numeric,
  p_payment_address text,
  p_payment_address_tag text,
  p_hold_minutes integer default 10
)
returns table (
  direct_order_id bigint,
  code text,
  payment_asset text,
  payment_network text,
  payment_amount_asset numeric,
  payment_address text,
  payment_address_tag text,
  created_at timestamptz,
  product_id bigint,
  product_name text,
  quantity integer,
  bonus_quantity integer,
  unit_price bigint,
  amount bigint,
  sale_campaign_id bigint,
  sale_item_id bigint,
  held_until timestamptz
)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_sale record;
  v_quantity integer := greatest(1, coalesce(p_quantity, 0));
  v_bonus_quantity integer := 0;
  v_required_stock integer;
  v_reservation_ids bigint[];
  v_stock_count integer;
  v_hold_until timestamptz := now() + make_interval(mins => greatest(1, coalesce(p_hold_minutes, 10)));
  v_order public.direct_orders%rowtype;
  v_user_limit integer;
  v_user_existing_quantity integer := 0;
begin
  if not (auth.role() = 'service_role' or public.is_admin()) then
    raise exception 'forbidden';
  end if;

  perform public.release_expired_sale_stock_holds(p_sale_item_id);

  select *
  into v_sale
  from public.get_active_sale_product(p_sale_item_id);

  if not found then
    raise exception 'sale_item_not_active';
  end if;

  v_bonus_quantity := case
    when coalesce(v_sale.promo_buy_quantity, 0) > 0 and coalesce(v_sale.promo_bonus_quantity, 0) > 0
      then (v_quantity / v_sale.promo_buy_quantity) * v_sale.promo_bonus_quantity
    else 0
  end;
  v_required_stock := greatest(1, v_quantity + greatest(0, v_bonus_quantity));

  v_user_limit := coalesce(v_sale.per_user_limit, 0);
  if v_user_limit > 0 then
    select coalesce(sum(qty), 0)::integer into v_user_existing_quantity
    from (
      select coalesce(o.sale_paid_quantity, o.quantity, 0) as qty
      from public.orders o
      where o.user_id = p_user_id and o.sale_item_id = p_sale_item_id
      union all
      select coalesce(d.sale_paid_quantity, d.quantity, 0) as qty
      from public.direct_orders d
      where d.user_id = p_user_id
        and d.sale_item_id = p_sale_item_id
        and d.status = 'pending'
    ) used;

    if v_user_existing_quantity + v_quantity > v_user_limit then
      raise exception 'sale_user_limit_exceeded';
    end if;
  end if;

  if v_required_stock > coalesce(v_sale.stock, 0) then
    raise exception 'not_enough_stock';
  end if;

  with locked_stock as (
    select r.id as reservation_id
    from public.sale_stock_reservations r
    join public.stock s on s.id = r.stock_id
    where r.sale_item_id = p_sale_item_id
      and r.released_at is null
      and r.status in ('available', 'held')
      and (r.status = 'available' or coalesce(r.held_until, now() - interval '1 second') <= now())
      and coalesce(s.sold, false) = false
    order by r.id
    for update skip locked
    limit v_required_stock
  )
  select array_agg(ls.reservation_id order by ls.reservation_id), count(*)
  into v_reservation_ids, v_stock_count
  from locked_stock ls;

  if coalesce(v_stock_count, 0) < v_required_stock then
    raise exception 'not_enough_stock';
  end if;

  insert into public.direct_orders (
    user_id,
    product_id,
    quantity,
    bonus_quantity,
    unit_price,
    amount,
    code,
    payment_channel,
    payment_asset,
    payment_network,
    payment_amount_asset,
    payment_rate_vnd,
    payment_address,
    payment_address_tag,
    created_at,
    sale_campaign_id,
    sale_item_id,
    sale_original_unit_price,
    sale_unit_price,
    sale_discount_amount,
    sale_paid_quantity,
    sale_bonus_quantity,
    sale_snapshot,
    sale_hold_until
  )
  values (
    p_user_id,
    v_sale.product_id,
    v_quantity,
    v_bonus_quantity,
    v_sale.price,
    v_sale.price * v_quantity,
    p_code,
    'binance_onchain',
    p_payment_asset,
    p_payment_network,
    p_payment_amount_asset,
    p_payment_rate_vnd,
    p_payment_address,
    nullif(p_payment_address_tag, ''),
    now(),
    v_sale.sale_campaign_id,
    p_sale_item_id,
    coalesce(v_sale.original_price, v_sale.price),
    v_sale.price,
    greatest(0, coalesce(v_sale.original_price, v_sale.price) - v_sale.price),
    v_quantity,
    v_bonus_quantity,
    jsonb_build_object(
      'campaign_id', v_sale.sale_campaign_id,
      'campaign_name', v_sale.campaign_name,
      'sale_item_id', p_sale_item_id,
      'product_id', v_sale.product_id,
      'product_name', v_sale.name,
      'sale_price_vnd', v_sale.price,
      'original_price_vnd', v_sale.original_price,
      'ends_at', v_sale.ends_at
    ),
    v_hold_until
  )
  returning * into v_order;

  update public.sale_stock_reservations
  set status = 'held',
      held_direct_order_id = v_order.id,
      held_until = v_hold_until
  where id = any(v_reservation_ids);

  return query
  select
    v_order.id,
    v_order.code,
    v_order.payment_asset,
    v_order.payment_network,
    v_order.payment_amount_asset,
    v_order.payment_address,
    v_order.payment_address_tag,
    v_order.created_at,
    v_sale.product_id,
    v_sale.name,
    v_quantity,
    v_bonus_quantity,
    v_sale.price,
    v_sale.price * v_quantity,
    v_sale.sale_campaign_id,
    p_sale_item_id,
    v_hold_until;
end;
$$;

create or replace function public.fulfill_bot_direct_order(
  p_direct_order_id bigint,
  p_order_group text default null,
  p_expire_minutes integer default 10
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_order public.direct_orders%rowtype;
  v_product_name text;
  v_description text;
  v_format_data text;
  v_deliver_quantity integer;
  v_order_group text;
  v_stock_ids bigint[];
  v_reservation_ids bigint[];
  v_items jsonb;
  v_stock_count integer;
  v_created_order_id bigint;
  v_total_price bigint;
begin
  if not (auth.role() = 'service_role' or public.is_admin()) then
    raise exception 'forbidden';
  end if;

  select *
  into v_order
  from public.direct_orders
  where id = p_direct_order_id
  for update;

  if not found then
    raise exception 'direct_order_not_found';
  end if;

  if coalesce(v_order.status, '') <> 'pending' then
    raise exception 'direct_order_not_pending';
  end if;

  if (
      v_order.created_at is not null
      and v_order.created_at <= now() - make_interval(mins => greatest(1, coalesce(p_expire_minutes, 10)))
    )
    or (
      v_order.sale_hold_until is not null
      and v_order.sale_hold_until <= now()
    )
  then
    update public.direct_orders
    set status = 'cancelled'
    where id = v_order.id;

    if v_order.sale_item_id is not null then
      update public.sale_stock_reservations
      set status = 'available',
          held_direct_order_id = null,
          held_until = null
      where held_direct_order_id = v_order.id
        and status = 'held';
    end if;

    raise exception 'direct_order_expired';
  end if;

  select p.name, p.description, p.format_data
  into v_product_name, v_description, v_format_data
  from public.products p
  where p.id = v_order.product_id;

  v_deliver_quantity := greatest(1, coalesce(v_order.quantity, 0) + greatest(0, coalesce(v_order.bonus_quantity, 0)));
  v_order_group := coalesce(
    nullif(btrim(p_order_group), ''),
    'PAY' || v_order.user_id::text || to_char(now(), 'YYYYMMDDHH24MISS')
  );
  v_total_price := coalesce(v_order.amount, coalesce(v_order.unit_price, 0) * greatest(1, coalesce(v_order.quantity, 0)));

  if v_order.sale_item_id is not null then
    with locked_stock as (
      select r.id as reservation_id, s.id as stock_id, s.content
      from public.sale_stock_reservations r
      join public.stock s on s.id = r.stock_id
      where r.held_direct_order_id = v_order.id
        and r.sale_item_id = v_order.sale_item_id
        and r.status = 'held'
        and coalesce(s.sold, false) = false
      order by r.id
      for update skip locked
    )
    select
      array_agg(ls.reservation_id order by ls.reservation_id),
      array_agg(ls.stock_id order by ls.reservation_id),
      coalesce(jsonb_agg(ls.content order by ls.reservation_id), '[]'::jsonb),
      count(*)
    into v_reservation_ids, v_stock_ids, v_items, v_stock_count
    from locked_stock ls;

    if coalesce(v_stock_count, 0) < v_deliver_quantity then
      update public.direct_orders
      set status = 'failed'
      where id = v_order.id;

      update public.sale_stock_reservations
      set status = 'available',
          held_direct_order_id = null,
          held_until = null
      where held_direct_order_id = v_order.id
        and status = 'held';

      raise exception 'not_enough_stock';
    end if;

    update public.stock
    set sold = true
    where id = any(v_stock_ids);

    insert into public.orders (
      user_id,
      product_id,
      content,
      price,
      quantity,
      order_group,
      created_at,
      sale_campaign_id,
      sale_item_id,
      sale_original_unit_price,
      sale_unit_price,
      sale_discount_amount,
      sale_paid_quantity,
      sale_bonus_quantity,
      sale_snapshot
    )
    values (
      v_order.user_id,
      v_order.product_id,
      v_items::text,
      v_total_price,
      v_deliver_quantity,
      v_order_group,
      now(),
      v_order.sale_campaign_id,
      v_order.sale_item_id,
      v_order.sale_original_unit_price,
      v_order.sale_unit_price,
      v_order.sale_discount_amount,
      coalesce(v_order.sale_paid_quantity, v_order.quantity),
      coalesce(v_order.sale_bonus_quantity, v_order.bonus_quantity),
      coalesce(v_order.sale_snapshot, '{}'::jsonb)
    )
    returning id into v_created_order_id;

    update public.sale_stock_reservations
    set status = 'sold',
        sold_order_id = v_created_order_id,
        sold_at = now(),
        held_direct_order_id = null,
        held_until = null
    where id = any(v_reservation_ids);

    update public.direct_orders
    set status = 'confirmed'
    where id = v_order.id;

    return jsonb_build_object(
      'direct_order_id', v_order.id,
      'order_id', v_created_order_id,
      'user_id', v_order.user_id,
      'product_id', v_order.product_id,
      'sale_campaign_id', v_order.sale_campaign_id,
      'sale_item_id', v_order.sale_item_id,
      'product_name', coalesce(nullif(btrim(v_product_name), ''), '#' || v_order.product_id::text),
      'description', coalesce(v_description, ''),
      'format_data', coalesce(v_format_data, ''),
      'quantity', coalesce(v_order.quantity, 1),
      'bonus_quantity', coalesce(v_order.bonus_quantity, 0),
      'delivered_quantity', v_deliver_quantity,
      'unit_price', coalesce(v_order.unit_price, 0),
      'amount', v_total_price,
      'code', coalesce(v_order.code, ''),
      'order_group', v_order_group,
      'items', coalesce(v_items, '[]'::jsonb),
      'sale_snapshot', coalesce(v_order.sale_snapshot, '{}'::jsonb)
    );
  end if;

  with locked_stock as (
    select s.id, s.content
    from public.stock s
    where s.product_id = v_order.product_id
      and s.sold = false
    order by s.id
    for update skip locked
    limit v_deliver_quantity
  )
  select
    array_agg(ls.id order by ls.id),
    coalesce(jsonb_agg(ls.content order by ls.id), '[]'::jsonb),
    count(*)
  into v_stock_ids, v_items, v_stock_count
  from locked_stock ls;

  if coalesce(v_stock_count, 0) < v_deliver_quantity then
    update public.direct_orders
    set status = 'failed'
    where id = v_order.id;

    raise exception 'not_enough_stock';
  end if;

  update public.stock
  set sold = true
  where id = any(v_stock_ids);

  insert into public.orders (
    user_id,
    product_id,
    content,
    price,
    quantity,
    order_group,
    created_at
  )
  values (
    v_order.user_id,
    v_order.product_id,
    v_items::text,
    v_total_price,
    v_deliver_quantity,
    v_order_group,
    now()
  )
  returning id into v_created_order_id;

  update public.direct_orders
  set status = 'confirmed'
  where id = v_order.id;

  return jsonb_build_object(
    'direct_order_id', v_order.id,
    'order_id', v_created_order_id,
    'user_id', v_order.user_id,
    'product_id', v_order.product_id,
    'product_name', coalesce(nullif(btrim(v_product_name), ''), '#' || v_order.product_id::text),
    'description', coalesce(v_description, ''),
    'format_data', coalesce(v_format_data, ''),
    'quantity', coalesce(v_order.quantity, 1),
    'bonus_quantity', coalesce(v_order.bonus_quantity, 0),
    'delivered_quantity', v_deliver_quantity,
    'unit_price', coalesce(v_order.unit_price, 0),
    'amount', v_total_price,
    'code', coalesce(v_order.code, ''),
    'order_group', v_order_group,
    'items', coalesce(v_items, '[]'::jsonb)
  );
end;
$$;

