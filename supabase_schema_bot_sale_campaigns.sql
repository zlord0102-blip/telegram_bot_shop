create or replace function public.release_expired_sale_stock_holds(
  p_sale_item_id bigint default null
)
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
  v_count integer := 0;
begin
  if not (auth.role() = 'service_role' or public.is_admin()) then
    raise exception 'forbidden';
  end if;

  update public.direct_orders d
  set status = 'cancelled'
  where d.status = 'pending'
    and d.sale_item_id is not null
    and d.sale_hold_until is not null
    and d.sale_hold_until <= now()
    and (p_sale_item_id is null or d.sale_item_id = p_sale_item_id);

  update public.sale_stock_reservations r
  set status = 'available',
      held_direct_order_id = null,
      held_until = null
  where r.status = 'held'
    and r.held_until is not null
    and r.held_until <= now()
    and (p_sale_item_id is null or r.sale_item_id = p_sale_item_id);

  get diagnostics v_count = row_count;
  return v_count;
end;
$$;

create or replace function public.get_active_sale_products()
returns table (
  sale_item_id bigint,
  sale_campaign_id bigint,
  product_id bigint,
  name text,
  telegram_icon text,
  telegram_icon_custom_emoji_id text,
  price bigint,
  price_usdt numeric,
  original_price bigint,
  original_price_usdt numeric,
  discount_percent numeric,
  price_tiers jsonb,
  promo_buy_quantity integer,
  promo_bonus_quantity integer,
  description text,
  format_data text,
  sort_position integer,
  stock bigint,
  campaign_name text,
  starts_at timestamptz,
  ends_at timestamptz,
  per_user_limit integer,
  quantity_limit integer,
  sold_quantity bigint
)
language sql
stable
security definer
set search_path = public
as $$
  with active_items as (
    select
      si.*,
      c.name as campaign_name,
      c.starts_at,
      c.ends_at,
      c.total_quantity_limit as campaign_quantity_limit,
      c.per_user_limit as campaign_per_user_limit
    from public.sale_items si
    join public.sale_campaigns c on c.id = si.campaign_id
    where si.is_enabled = true
      and c.status in ('active', 'scheduled')
      and c.starts_at <= now()
      and c.ends_at > now()
  ),
  reservation_counts as (
    select
      r.sale_item_id,
      count(*) filter (
        where r.released_at is null
          and r.status in ('available', 'held')
          and (r.status = 'available' or coalesce(r.held_until, now() - interval '1 second') <= now())
          and coalesce(s.sold, false) = false
      )::bigint as available_stock,
      count(*) filter (where r.status = 'sold')::bigint as sold_stock
    from public.sale_stock_reservations r
    join public.stock s on s.id = r.stock_id
    group by r.sale_item_id
  ),
  campaign_sold as (
    select
      si.campaign_id,
      count(*) filter (where r.status = 'sold')::bigint as sold_stock
    from public.sale_items si
    left join public.sale_stock_reservations r on r.sale_item_id = si.id
    group by si.campaign_id
  )
  select
    ai.id as sale_item_id,
    ai.campaign_id as sale_campaign_id,
    p.id as product_id,
    coalesce(nullif(btrim(ai.sale_name), ''), p.name) as name,
    coalesce(nullif(btrim(ai.telegram_icon), ''), nullif(btrim(p.telegram_icon), ''), 'SALE') as telegram_icon,
    coalesce(
      nullif(btrim(ai.telegram_icon_custom_emoji_id), ''),
      nullif(btrim(c.default_telegram_icon_custom_emoji_id), ''),
      nullif(btrim(p.telegram_icon_custom_emoji_id), ''),
      '6055192572056309981'
    ) as telegram_icon_custom_emoji_id,
    ai.sale_price_vnd as price,
    coalesce(ai.sale_price_usdt, p.price_usdt) as price_usdt,
    coalesce(ai.original_price_vnd, p.price) as original_price,
    coalesce(ai.original_price_usdt, p.price_usdt) as original_price_usdt,
    ai.discount_percent,
    null::jsonb as price_tiers,
    coalesce(ai.promo_buy_quantity, 0) as promo_buy_quantity,
    coalesce(ai.promo_bonus_quantity, 0) as promo_bonus_quantity,
    coalesce(nullif(btrim(ai.sale_description), ''), p.description) as description,
    p.format_data,
    ai.sort_position,
    greatest(
      0,
      least(
        coalesce(rc.available_stock, 0),
        coalesce(ai.quantity_limit, 2147483647) - coalesce(rc.sold_stock, 0),
        coalesce(ai.campaign_quantity_limit, 2147483647) - coalesce(cs.sold_stock, 0)
      )
    )::bigint as stock,
    ai.campaign_name,
    ai.starts_at,
    ai.ends_at,
    coalesce(ai.per_user_limit, ai.campaign_per_user_limit) as per_user_limit,
    ai.quantity_limit,
    coalesce(rc.sold_stock, 0) as sold_quantity
  from active_items ai
  join public.sale_campaigns c on c.id = ai.campaign_id
  join public.products p on p.id = ai.product_id
  left join reservation_counts rc on rc.sale_item_id = ai.id
  left join campaign_sold cs on cs.campaign_id = ai.campaign_id
  where coalesce(p.is_deleted, false) = false
    and coalesce(p.is_hidden, false) = false
  order by
    case when ai.sort_position is null then 1 else 0 end,
    ai.sort_position nulls last,
    ai.id;
$$;

