-- Telegram Bot Sale campaigns.
-- Apply after base product/order/direct-order/stock schema.

create table if not exists public.sale_campaigns (
  id bigint generated by default as identity primary key,
  name text not null,
  status text not null default 'draft'
    check (status in ('draft', 'scheduled', 'active', 'paused', 'ended', 'cancelled')),
  starts_at timestamptz not null,
  ends_at timestamptz not null,
  timezone text not null default 'Asia/Ho_Chi_Minh',
  default_telegram_icon text default 'SALE',
  default_telegram_icon_custom_emoji_id text default '6055192572056309981',
  total_quantity_limit integer,
  per_user_limit integer,
  notify_on_start boolean not null default false,
  notify_ending_soon boolean not null default false,
  notes text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (ends_at > starts_at),
  check (total_quantity_limit is null or total_quantity_limit > 0),
  check (per_user_limit is null or per_user_limit > 0)
);

create index if not exists sale_campaigns_status_time_idx
  on public.sale_campaigns (status, starts_at, ends_at);

create table if not exists public.sale_items (
  id bigint generated by default as identity primary key,
  campaign_id bigint not null references public.sale_campaigns(id) on delete cascade,
  product_id bigint not null references public.products(id) on delete cascade,
  sale_name text,
  sale_description text,
  sale_price_vnd bigint not null,
  sale_price_usdt numeric,
  original_price_vnd bigint,
  original_price_usdt numeric,
  discount_percent numeric,
  promo_buy_quantity integer not null default 0,
  promo_bonus_quantity integer not null default 0,
  stock_mode text not null default 'reserved_existing'
    check (stock_mode in ('reserved_existing', 'new_stock', 'mixed')),
  quantity_limit integer,
  per_user_limit integer,
  telegram_icon text default 'SALE',
  telegram_icon_custom_emoji_id text default '6055192572056309981',
  is_enabled boolean not null default true,
  sort_position integer,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  check (sale_price_vnd >= 0),
  check (sale_price_usdt is null or sale_price_usdt >= 0),
  check (promo_buy_quantity >= 0),
  check (promo_bonus_quantity >= 0),
  check (quantity_limit is null or quantity_limit > 0),
  check (per_user_limit is null or per_user_limit > 0)
);

create unique index if not exists sale_items_campaign_product_idx
  on public.sale_items (campaign_id, product_id);

create index if not exists sale_items_campaign_sort_idx
  on public.sale_items (campaign_id, sort_position, id);

create index if not exists sale_items_product_idx
  on public.sale_items (product_id);

create table if not exists public.sale_stock_reservations (
  id bigint generated by default as identity primary key,
  sale_item_id bigint not null references public.sale_items(id) on delete cascade,
  stock_id bigint not null references public.stock(id) on delete cascade,
  status text not null default 'available'
    check (status in ('available', 'held', 'sold', 'released')),
  held_direct_order_id bigint references public.direct_orders(id) on delete set null,
  held_until timestamptz,
  sold_order_id bigint references public.orders(id) on delete set null,
  sold_at timestamptz,
  released_at timestamptz,
  created_at timestamptz not null default now()
);

create unique index if not exists sale_stock_reservations_live_stock_idx
  on public.sale_stock_reservations (stock_id)
  where released_at is null and status in ('available', 'held', 'sold');

create index if not exists sale_stock_reservations_item_status_idx
  on public.sale_stock_reservations (sale_item_id, status, held_until, id);

