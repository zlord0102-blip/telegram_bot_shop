create or replace function public.fulfill_bot_sale_balance_purchase(
  p_user_id bigint,
  p_sale_item_id bigint,
  p_quantity integer,
  p_charge_currency text default 'vnd',
  p_order_group text default null
)
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_sale record;
  v_balance bigint;
  v_balance_usdt numeric;
  v_quantity integer := greatest(1, coalesce(p_quantity, 0));
  v_bonus_quantity integer := 0;
  v_required_stock integer;
  v_user_limit integer;
  v_user_existing_quantity integer := 0;
  v_unit_price_vnd bigint;
  v_total_price_vnd bigint;
  v_unit_price_usdt numeric;
  v_total_price_usdt numeric;
  v_charge_currency text := lower(coalesce(nullif(btrim(p_charge_currency), ''), 'vnd'));
  v_stock_ids bigint[];
  v_reservation_ids bigint[];
  v_items jsonb;
  v_stock_count integer;
  v_order_group text;
  v_created_order_id bigint;
  v_new_balance bigint;
  v_new_balance_usdt numeric;
begin
  if not (auth.role() = 'service_role' or public.is_admin()) then
    raise exception 'forbidden';
  end if;

  perform public.release_expired_sale_stock_holds(p_sale_item_id);

  select *
  into v_sale
  from public.get_active_sale_product(p_sale_item_id);

  if not found then
    raise exception 'sale_item_not_active';
  end if;

  if coalesce(v_sale.stock, 0) < 1 then
    raise exception 'not_enough_stock';
  end if;

  insert into public.users (user_id, created_at)
  values (p_user_id, now())
  on conflict (user_id) do nothing;

  v_bonus_quantity := case
    when coalesce(v_sale.promo_buy_quantity, 0) > 0 and coalesce(v_sale.promo_bonus_quantity, 0) > 0
      then (v_quantity / v_sale.promo_buy_quantity) * v_sale.promo_bonus_quantity
    else 0
  end;
  v_required_stock := greatest(1, v_quantity + greatest(0, v_bonus_quantity));

  v_user_limit := coalesce(v_sale.per_user_limit, 0);
  if v_user_limit > 0 then
    select coalesce(sum(qty), 0)::integer into v_user_existing_quantity
    from (
      select coalesce(o.sale_paid_quantity, o.quantity, 0) as qty
      from public.orders o
      where o.user_id = p_user_id and o.sale_item_id = p_sale_item_id
      union all
      select coalesce(d.sale_paid_quantity, d.quantity, 0) as qty
      from public.direct_orders d
      where d.user_id = p_user_id
        and d.sale_item_id = p_sale_item_id
        and d.status = 'pending'
    ) used;

    if v_user_existing_quantity + v_quantity > v_user_limit then
      raise exception 'sale_user_limit_exceeded';
    end if;
  end if;

  if v_required_stock > coalesce(v_sale.stock, 0) then
    raise exception 'not_enough_stock';
  end if;

  select
    coalesce(u.balance, 0),
    coalesce(u.balance_usdt, 0)
  into v_balance, v_balance_usdt
  from public.users u
  where u.user_id = p_user_id
  for update;

  if not found then
    raise exception 'user_not_found';
  end if;

  v_unit_price_vnd := greatest(0, coalesce(v_sale.price, 0));
  v_total_price_vnd := v_unit_price_vnd * v_quantity;
  v_unit_price_usdt := greatest(0, coalesce(v_sale.price_usdt, 0));
  v_total_price_usdt := v_unit_price_usdt * v_quantity;

  if v_charge_currency = 'usdt' then
    if v_unit_price_usdt <= 0 then
      raise exception 'sale_usdt_not_available';
    end if;
    if v_balance_usdt < v_total_price_usdt then
      raise exception 'insufficient_usdt_balance';
    end if;
  else
    v_charge_currency := 'vnd';
    if v_balance < v_total_price_vnd then
      raise exception 'insufficient_balance';
    end if;
  end if;

  with locked_stock as (
    select r.id as reservation_id, s.id as stock_id, s.content
    from public.sale_stock_reservations r
    join public.stock s on s.id = r.stock_id
    where r.sale_item_id = p_sale_item_id
      and r.released_at is null
      and r.status in ('available', 'held')
      and (r.status = 'available' or coalesce(r.held_until, now() - interval '1 second') <= now())
      and coalesce(s.sold, false) = false
    order by r.id
    for update skip locked
    limit v_required_stock
  )
  select
    array_agg(ls.reservation_id order by ls.reservation_id),
    array_agg(ls.stock_id order by ls.reservation_id),
    coalesce(jsonb_agg(ls.content order by ls.reservation_id), '[]'::jsonb),
    count(*)
  into v_reservation_ids, v_stock_ids, v_items, v_stock_count
  from locked_stock ls;

  if coalesce(v_stock_count, 0) < v_required_stock then
    raise exception 'not_enough_stock';
  end if;

  update public.stock
  set sold = true
  where id = any(v_stock_ids);

  update public.users
  set balance = case when v_charge_currency = 'vnd' then coalesce(balance, 0) - v_total_price_vnd else coalesce(balance, 0) end,
      balance_usdt = case when v_charge_currency = 'usdt' then coalesce(balance_usdt, 0) - v_total_price_usdt else coalesce(balance_usdt, 0) end
  where user_id = p_user_id
  returning balance, balance_usdt into v_new_balance, v_new_balance_usdt;

  v_order_group := coalesce(
    nullif(btrim(p_order_group), ''),
    'SALE' || p_user_id::text || to_char(now(), 'YYYYMMDDHH24MISS')
  );

  insert into public.orders (
    user_id,
    product_id,
    content,
    price,
    quantity,
    order_group,
    created_at,
    sale_campaign_id,
    sale_item_id,
    sale_original_unit_price,
    sale_unit_price,
    sale_discount_amount,
    sale_paid_quantity,
    sale_bonus_quantity,
    sale_snapshot
  )
  values (
    p_user_id,
    v_sale.product_id,
    v_items::text,
    case when v_charge_currency = 'usdt' then v_total_price_vnd else v_total_price_vnd end,
    v_required_stock,
    v_order_group,
    now(),
    v_sale.sale_campaign_id,
    p_sale_item_id,
    coalesce(v_sale.original_price, v_unit_price_vnd),
    v_unit_price_vnd,
    greatest(0, coalesce(v_sale.original_price, v_unit_price_vnd) - v_unit_price_vnd),
    v_quantity,
    v_bonus_quantity,
    jsonb_build_object(
      'campaign_id', v_sale.sale_campaign_id,
      'campaign_name', v_sale.campaign_name,
      'sale_item_id', p_sale_item_id,
      'product_id', v_sale.product_id,
      'product_name', v_sale.name,
      'charge_currency', v_charge_currency,
      'sale_price_vnd', v_unit_price_vnd,
      'sale_price_usdt', v_unit_price_usdt,
      'original_price_vnd', v_sale.original_price,
      'ends_at', v_sale.ends_at
    )
  )
  returning id into v_created_order_id;

  update public.sale_stock_reservations
  set status = 'sold',
      sold_order_id = v_created_order_id,
      sold_at = now(),
      held_direct_order_id = null,
      held_until = null
  where id = any(v_reservation_ids);

  return jsonb_build_object(
    'order_id', v_created_order_id,
    'user_id', p_user_id,
    'product_id', v_sale.product_id,
    'sale_campaign_id', v_sale.sale_campaign_id,
    'sale_item_id', p_sale_item_id,
    'product_name', coalesce(nullif(btrim(v_sale.name), ''), '#' || v_sale.product_id::text),
    'description', coalesce(v_sale.description, ''),
    'format_data', coalesce(v_sale.format_data, ''),
    'quantity', v_quantity,
    'bonus_quantity', v_bonus_quantity,
    'delivered_quantity', v_required_stock,
    'order_group', v_order_group,
    'items', coalesce(v_items, '[]'::jsonb),
    'order_total_price', v_total_price_vnd,
    'charged_balance', case when v_charge_currency = 'vnd' then v_total_price_vnd else 0 end,
    'charged_balance_usdt', case when v_charge_currency = 'usdt' then v_total_price_usdt else 0 end,
    'new_balance', coalesce(v_new_balance, 0),
    'new_balance_usdt', coalesce(v_new_balance_usdt, 0),
    'charge_currency', v_charge_currency,
    'sale_ends_at', v_sale.ends_at
  );
end;
$$;

