CREATE OR REPLACE FUNCTION public.prevent_license_extension_code_change()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  IF TG_OP = 'UPDATE' AND OLD.code IS DISTINCT FROM NEW.code THEN
    RAISE EXCEPTION 'extension_code_immutable';
  END IF;

  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_license_extensions_code_immutable ON public.license_extensions;
CREATE TRIGGER trg_license_extensions_code_immutable
BEFORE UPDATE ON public.license_extensions
FOR EACH ROW
EXECUTE FUNCTION public.prevent_license_extension_code_change();

DROP TRIGGER IF EXISTS trg_license_extensions_updated_at ON public.license_extensions;
CREATE TRIGGER trg_license_extensions_updated_at
BEFORE UPDATE ON public.license_extensions
