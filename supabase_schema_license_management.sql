ALTER TABLE public.license_keys
  ADD CONSTRAINT license_keys_device_limit_mode_chk
  CHECK (device_limit_mode IN ('single_device', 'unlimited_devices'));

CREATE TABLE IF NOT EXISTS public.license_activations (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  license_key_id BIGINT NOT NULL REFERENCES public.license_keys(id) ON UPDATE CASCADE ON DELETE CASCADE,
  fingerprint TEXT NOT NULL,
  activation_token_hash TEXT NOT NULL UNIQUE,
  activated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  last_checked_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  deactivated_at TIMESTAMPTZ,
  deactivation_reason TEXT,
  last_ip TEXT,
  last_user_agent TEXT,
  last_version TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW()
);

CREATE TABLE IF NOT EXISTS public.license_check_logs (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  license_key_id BIGINT REFERENCES public.license_keys(id) ON UPDATE CASCADE ON DELETE SET NULL,
  extension_code TEXT NOT NULL,
  fingerprint TEXT,
  request_type TEXT NOT NULL,
  result_status TEXT NOT NULL,
  ip TEXT,
  user_agent TEXT,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT license_check_logs_request_type_chk CHECK (request_type IN ('activate', 'validate')),
  CONSTRAINT license_check_logs_result_status_chk CHECK (
    result_status IN ('active', 'expired', 'revoked', 'extension_disabled', 'fingerprint_mismatch', 'not_found')
  )
);

