-- License management for external extensions

CREATE OR REPLACE FUNCTION public.set_row_updated_at()
RETURNS trigger
LANGUAGE plpgsql
AS $$
BEGIN
  NEW.updated_at = NOW();
  RETURN NEW;
END;
$$;

CREATE TABLE IF NOT EXISTS public.license_extensions (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  code TEXT NOT NULL UNIQUE,
  name TEXT NOT NULL,
  description TEXT,
  is_active BOOLEAN NOT NULL DEFAULT TRUE,
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT license_extensions_code_format_chk CHECK (code ~ '^[A-Z0-9_-]+$')
);

CREATE TABLE IF NOT EXISTS public.license_keys (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  extension_id BIGINT NOT NULL REFERENCES public.license_extensions(id) ON UPDATE CASCADE ON DELETE RESTRICT,
  key_hash TEXT NOT NULL UNIQUE,
  key_prefix TEXT NOT NULL,
  key_suffix TEXT NOT NULL,
  status TEXT NOT NULL DEFAULT 'active',
  expires_at TIMESTAMPTZ NOT NULL,
  note TEXT,
  device_limit_mode TEXT NOT NULL DEFAULT 'single_device',
  created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  CONSTRAINT license_keys_status_chk CHECK (status IN ('active', 'expired', 'revoked')),
  CONSTRAINT license_keys_device_limit_mode_chk CHECK (device_limit_mode IN ('single_device', 'unlimited_devices'))
);

