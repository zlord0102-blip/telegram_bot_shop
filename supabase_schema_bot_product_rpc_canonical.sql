  on public.bot_product_folders (lower(btrim(name)));

create index if not exists idx_bot_product_folders_sort
  on public.bot_product_folders (sort_position, id);

do $$
begin
  if not exists (
    select 1
    from pg_constraint
    where conname = 'products_bot_folder_id_fkey'
  ) then
    alter table public.products
      add constraint products_bot_folder_id_fkey
      foreign key (bot_folder_id)
      references public.bot_product_folders(id)
      on delete set null;
  end if;
end $$;

