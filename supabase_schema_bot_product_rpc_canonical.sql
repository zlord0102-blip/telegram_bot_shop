update public.products
set
  is_hidden = coalesce(is_hidden, false),
  is_deleted = coalesce(is_deleted, false),
  promo_buy_quantity = coalesce(promo_buy_quantity, 0),
  promo_bonus_quantity = coalesce(promo_bonus_quantity, 0),
  website_promo_buy_quantity = coalesce(website_promo_buy_quantity, 0),
  website_promo_bonus_quantity = coalesce(website_promo_bonus_quantity, 0),
  website_enabled = coalesce(website_enabled, true)
where
  is_hidden is null
  or is_deleted is null
  or promo_buy_quantity is null
  or promo_bonus_quantity is null
  or website_promo_buy_quantity is null
  or website_promo_bonus_quantity is null
  or website_enabled is null;

drop function if exists public.get_products_with_stock();
create or replace function public.get_products_with_stock()
returns table (
  id bigint,
  name text,
  telegram_icon text,
  telegram_icon_custom_emoji_id text,
  price bigint,
  price_usdt numeric,
  price_tiers jsonb,
  promo_buy_quantity integer,
  promo_bonus_quantity integer,
  website_name text,
  website_price bigint,
  website_price_tiers jsonb,
  website_promo_buy_quantity integer,
  website_promo_bonus_quantity integer,
  website_banner_url text,
  website_logo_url text,
  website_enabled boolean,
  description text,
  format_data text,
  sort_position integer,
  bot_folder_id bigint,
  stock bigint
)
language sql
stable
security definer
set search_path = public
as $$
  select
    p.id,
    p.name,
    p.telegram_icon,
    p.telegram_icon_custom_emoji_id,
    p.price,
    p.price_usdt,
    p.price_tiers,
    coalesce(p.promo_buy_quantity, 0) as promo_buy_quantity,
    coalesce(p.promo_bonus_quantity, 0) as promo_bonus_quantity,
    p.website_name,
    p.website_price,
    p.website_price_tiers,
    coalesce(p.website_promo_buy_quantity, 0) as website_promo_buy_quantity,
    coalesce(p.website_promo_bonus_quantity, 0) as website_promo_bonus_quantity,
    p.website_banner_url,
    p.website_logo_url,
    coalesce(p.website_enabled, true) as website_enabled,
    p.description,
    p.format_data,
    p.sort_position,
    p.bot_folder_id,
    coalesce(s.stock, 0) as stock
  from public.products p
  left join (
    select product_id, count(*) as stock
    from public.stock
    where sold = false
    group by product_id
  ) s on s.product_id = p.id
  where (auth.role() = 'service_role' or public.is_admin())
    and coalesce(p.is_deleted, false) = false
    and coalesce(p.is_hidden, false) = false
  order by
    case when p.sort_position is null then 1 else 0 end,
    p.sort_position nulls last,
    p.id;
$$;

